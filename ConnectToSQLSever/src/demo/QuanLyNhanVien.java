    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(txtMaNV.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Mã nhân viên không được rỗng");
            txtMaNV.requestFocus();
            return;
        }else if(txtHoTen.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Họ tên không được rỗng");
               txtHoTen.requestFocus();
               return;
        }else if(txtEmail.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Email không được rỗng");
               txtEmail.requestFocus();
               return;
        }else if(txtSDT.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Số điện thoại không được rỗng");
               txtSDT.requestFocus();
               return;
        }
        String manv = txtMaNV.getText();
        if(manv.length() != 4){
            JOptionPane.showMessageDialog(null, "Mã nhân viên phải có 4 ký tự");
            txtMaNV.requestFocus();
            return;
        }
        
        boolean b1 = Character.isLetter(manv.charAt(0));
        boolean b2 = Character.isLetter(manv.charAt(1));
        boolean b3 = Character.isDigit(manv.charAt(2));
        boolean b4 = Character.isDigit(manv.charAt(3));
        if((b1 && b2 && b3 && b4) == false){
            JOptionPane.showMessageDialog(null, "Mã nhân viên không đúng định dạng. Mẫu NV01!");
            txtMaNV.requestFocus();
            return;
        }
        if(txtEmail.getText().matches("^[a-z][a-z0-9_\\.]{5,32}@[a-z0-9]{2,}(\\.[a-z0-9]{2,4}){1,2}$") == false){
            JOptionPane.showMessageDialog(null, "Email không đúng định dạng");
            txtEmail.requestFocus();
            return;
        }
        if(txtSDT.getText().matches("(\\+84|0)\\d{9,10}") == false){
            JOptionPane.showMessageDialog(null, "Số điện thoại không đúng định dạng");
            txtSDT.requestFocus();
            return;
        }
        if(checkTtrungMaNV() == true){
            JOptionPane.showMessageDialog(null, "Mã nhân viên đã tồn tại");
            txtMaNV.requestFocus();
            return;
        }else{
            try {
                Connection connection = DBConnection.getConnection();
                String query ="INSERT INTO NHANVIEN (MaNV, HoTen, Email, SDT)"
                                 +"VALUES(?,?,?,?)";
                PreparedStatement ps = connection.prepareStatement(query);
                ps.setString(1, txtMaNV.getText());
                ps.setString(2, txtHoTen.getText());
                ps.setString(3, txtEmail.getText());
                ps.setString(4, txtSDT.getText());
                ps.executeUpdate();
                showDuLieu();
                JOptionPane.showMessageDialog(null, "Update database");
                DBConnection.closeConnection(connection);
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

