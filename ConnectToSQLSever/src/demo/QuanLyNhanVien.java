    private void showDuLieu(){
        try{
        tableMain.removeAll();
        String[] arr = {"Mã nhân viên", "Họ & Tên", "Email", "SDT"};
        DefaultTableModel model = new DefaultTableModel(arr, 0);
        
        Connection connection = DBConnection.getConnection();
        String query ="SELECT * FROM NHANVIEN";
        PreparedStatement ps = connection.prepareStatement(query);
        ResultSet rs = ps.executeQuery();
        while(rs.next()){
            Vector vector = new Vector();
            vector.add(rs.getString("MaNV"));
            vector.add(rs.getString("HoTen"));
            vector.add(rs.getString("Email"));
            vector.add(rs.getString("SDT"));
            model.addRow(vector); 
        }
        DBConnection.closeConnection(connection);
        tableMain.setModel(model);
        }catch(SQLException ex){
          Logger.getLogger(QuanLyNhanVien.class.getName()).log(Level.SEVERE, null, ex);  
        }
    }
    
      private boolean checkTtrungMaNV(){
        try {
            Connection connection = DBConnection.getConnection();
            String query ="SELECT * FROM NHANVIEN WHERE MaNV = ?";
            PreparedStatement ps = connection.prepareStatement(query);
            ps.setString(1, txtMaNV.getText());
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
               return true;
            }
            DBConnection.closeConnection(connection);
        } catch (SQLException ex) {
            Logger.getLogger(QuanLyNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
      
